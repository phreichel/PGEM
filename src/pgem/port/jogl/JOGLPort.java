//*************************************************************************************************
package pgem.port.jogl;
//*************************************************************************************************

import pgem.msg.MsgHub;
import pgem.port.Port;
import pgem.port.Window;
import pgem.render.Renderer;
import pgem.render.jogl.JOGLGraphics;

//*************************************************************************************************
public class JOGLPort implements Port {

	//=============================================================================================
	private final JOGLInput input = new JOGLInput();
	private final JOGLGraphics graphics = new JOGLGraphics();
	private final JOGLWindow window = new JOGLWindow(input, graphics);
	//=============================================================================================

	//=============================================================================================
	public Window window() {
		return window;
	}
	//=============================================================================================

	//=============================================================================================
	public Port link(Renderer r) {
		graphics.link(r);
		return this;
	}
	//=============================================================================================

	//=============================================================================================
	public Port unlink(Renderer r) {
		graphics.unlink(r);
		return this;
	}
	//=============================================================================================

	//=============================================================================================
	public Port render() {
		window.render();
		return this;
	}
	//=============================================================================================

	//=============================================================================================
	public Port link(MsgHub msgHub) {
		window.link(msgHub);
		input.link(msgHub);
		return this;
	}
	//=============================================================================================

	//=============================================================================================
	public Port unlink(MsgHub msgHub) {
		window.unlink(msgHub);
		input.unlink(msgHub);
		return this;
	}
	//=============================================================================================
	
}
//*************************************************************************************************
