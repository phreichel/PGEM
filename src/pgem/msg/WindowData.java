//*************************************************************************************************
package pgem.msg;
//*************************************************************************************************

import javax.vecmath.Vector2f;

//*************************************************************************************************
public class WindowData implements MsgData {

	//=============================================================================================
	public String title;
	public boolean state;
	public final Vector2f size = new Vector2f();
	//=============================================================================================
	
	//=============================================================================================
	public void clear() {
		title = null;
		state = false;
		size.set(0, 0);
	}
	//=============================================================================================

}
