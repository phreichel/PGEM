//*************************************************************************************************
package pgem.msg;
//*************************************************************************************************

//*************************************************************************************************
public final class Msg {

	//=============================================================================================
	public long    time;
	public MsgType type;
	public MsgData data;
	public MsgBox  msgbox;
	public boolean consumed;
	//=============================================================================================

	//=============================================================================================
	Msg() {}
	//=============================================================================================
	
	//=============================================================================================
	public void clear() {
		consumed = false;
		time = -1L;
		type = MsgType.NONE;
		msgbox = null;
		data = null;
	}
	//=============================================================================================
	
	//=============================================================================================
	@SuppressWarnings("unchecked")
	public <T> T data(Class<T> cls) {
		return (T) data;
	}
	//=============================================================================================
	
}
//*************************************************************************************************
