//*************************************************************************************************
package pgem.msg;
//*************************************************************************************************

import java.util.EnumSet;
import java.util.Set;

//*************************************************************************************************
public enum MsgType {
	
	//=============================================================================================
	NONE,
	//=============================================================================================

	//=============================================================================================
	KBD_PRESSED,
	KBD_RELEASED,
	KBD_TYPED,
	//=============================================================================================

	//=============================================================================================
	PTR_PRESSED,
	PTR_RELEASED,
	PTR_CLICKED,
	PTR_MOVED,
	//=============================================================================================

	//=============================================================================================
	WND_RESIZE,
	WND_CLOSE;
	//=============================================================================================

	//=============================================================================================
	public static final Set<MsgType> KBD_MASK = EnumSet.of(
		KBD_PRESSED,
		KBD_RELEASED,
		KBD_TYPED
	);
	//=============================================================================================
	
	//=============================================================================================
	public static final Set<MsgType> PTR_MASK = EnumSet.of(
		PTR_PRESSED,
		PTR_RELEASED,
		PTR_CLICKED,
		PTR_MOVED
	);
	//=============================================================================================
	
}
