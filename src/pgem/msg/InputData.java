//*************************************************************************************************
package pgem.msg;
//*************************************************************************************************

import java.util.EnumMap;
import java.util.EnumSet;
import java.util.Map;
import java.util.Set;

//*************************************************************************************************
public final class InputData implements MsgData {

	//=============================================================================================
	public Button button = Button.NONE;
	public final Set<Button> buttons = EnumSet.noneOf(Button.class); 
	//=============================================================================================

	//=============================================================================================
	public final Map<Axis, Float> axes = new EnumMap<>(Axis.class);
	//=============================================================================================
	
	//=============================================================================================
	public char keyCharacter = '\0'; 
	public int clickCount = 0;
	//=============================================================================================

	//=============================================================================================
	public InputData() {}
	//=============================================================================================

	//=============================================================================================
	public void clear() {
		button = Button.NONE;
		axes.clear();
		keyCharacter = '\0';
		clickCount = 0;
	}	
	//=============================================================================================
	
}
//*************************************************************************************************
