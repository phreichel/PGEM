//*************************************************************************************************
package pgem.noise;
//*************************************************************************************************

//*************************************************************************************************
public class Offset implements Module {

	//=============================================================================================
	public double offset = 1f;
	public final Module module;
	//=============================================================================================

	//=============================================================================================
	public Offset(Module module) {
		this.module = module;
	}
	//=============================================================================================

	//=============================================================================================
	public Offset(Module module, double offset) {
		this.offset = offset;
		this.module = module;
	}
	//=============================================================================================
	
	//=============================================================================================
	public double noise(double x, double y, double z) {
		return module.noise(x, y, z) + offset;
	}
	//=============================================================================================

}
//*************************************************************************************************
