//*************************************************************************************************
package pgem.noise;
//*************************************************************************************************

//*************************************************************************************************
public class Multiply implements Module {

	//=============================================================================================
	public final Module m1;
	public final Module m2;
	//=============================================================================================

	//=============================================================================================
	public Multiply(Module m1, Module m2) {
		this.m1 = m1;
		this.m2 = m2;
	}
	//=============================================================================================
	
	//=============================================================================================
	public double noise(double x, double y, double z) {
		return m1.noise(x, y, z) * m2.noise(x, y, z);
	}
	//=============================================================================================

}
//*************************************************************************************************
