//*************************************************************************************************
package pgem.noise;
//*************************************************************************************************

//*************************************************************************************************
public class Absolute implements Module {

	//=============================================================================================
	public final Module module;
	//=============================================================================================

	//=============================================================================================
	public Absolute(Module module) {
		this.module = module;
	}
	//=============================================================================================
	
	//=============================================================================================
	public double noise(double x, double y, double z) {
		return Math.abs(module.noise(x, y, z));
	}
	//=============================================================================================

}
