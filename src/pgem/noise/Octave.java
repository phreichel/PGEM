//*************************************************************************************************
package pgem.noise;
//*************************************************************************************************

//*************************************************************************************************
public class Octave implements Module {

	//=============================================================================================
	public final int    octaves;
	public final Module module;
	//=============================================================================================

	//=============================================================================================
	public Octave(Module module, int octaves) {
		this.module= module;
		this.octaves = octaves;
	}
	//=============================================================================================
	
	//=============================================================================================
	public double noise(double x, double y, double z) {
		float noise = 0f;
		for (int i=0; i<octaves; i++) {
			double s = 1f / (i+1);
			noise += module.noise(x * s, y * s, z * s) * s;
		}
		return noise;
	}
	//=============================================================================================

}
//*************************************************************************************************
