//*************************************************************************************************
package pgem.noise;
//*************************************************************************************************

//*************************************************************************************************
public class Clamp implements Module {

	//=============================================================================================
	public double upper =  Double.MAX_VALUE;
	public double lower = -Double.MAX_VALUE;
	public final Module module;
	//=============================================================================================

	//=============================================================================================
	public Clamp(Module module) {
		this.module = module;
	}
	//=============================================================================================

	//=============================================================================================
	public Clamp(Module module, double lower, double upper) {
		this.lower = lower;
		this.upper = upper;
		this.module = module;
	}
	//=============================================================================================
	
	//=============================================================================================
	public double noise(double x, double y, double z) {
		double value = module.noise(x, y, z);
		value = Math.max(lower, value);
		value = Math.min(upper, value);
		return value;
	}
	//=============================================================================================

}
