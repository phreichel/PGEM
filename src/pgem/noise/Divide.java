//*************************************************************************************************
package pgem.noise;
//*************************************************************************************************

//*************************************************************************************************
public class Divide implements Module {

	//=============================================================================================
	public final Module m1;
	public final Module m2;
	//=============================================================================================

	//=============================================================================================
	public Divide(Module m1, Module m2) {
		this.m1 = m1;
		this.m2 = m2;
	}
	//=============================================================================================
	
	//=============================================================================================
	public double noise(double x, double y, double z) {
		double b = m2.noise(x, y, z);
		if (b == 0) return 0;
		double a = m1.noise(x, y, z);
		return a / b;
	}
	//=============================================================================================

}
