//*************************************************************************************************
package pgem.model.physics;
//*************************************************************************************************

//*************************************************************************************************
public class Space {

	//=============================================================================================
	public final SpaceNode root;
	//=============================================================================================

	//=============================================================================================
	public Space() {
		root = new SpaceNode();
		root.boundary.set(
			-Float.MAX_VALUE,
			-Float.MAX_VALUE,
			-Float.MAX_VALUE,
			+Float.MAX_VALUE,
			+Float.MAX_VALUE,
			+Float.MAX_VALUE);
	}
	//=============================================================================================

	//=============================================================================================
	public boolean insert(Body body) {
		return root.insert(body);
	}
	//=============================================================================================

	//=============================================================================================
	public boolean delete(Body body) {
		return root.delete(body);
	}
	//=============================================================================================
	
}
//*************************************************************************************************
