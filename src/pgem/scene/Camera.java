//*************************************************************************************************
package pgem.scene;
//*************************************************************************************************

//*************************************************************************************************
public class Camera extends Node {

	//=============================================================================================
	public Camera() {
		super();
	}
	//=============================================================================================
	
	//=============================================================================================
	public Camera(Node parent) {
		super(parent);
	}
	//=============================================================================================

	//=============================================================================================
	public float fovy = 65f;
	public float near = .6f;
	public float far = 1000f;
	//=============================================================================================

	//=============================================================================================
	public void set(float fovy, float near, float far) {
		this.fovy = fovy;
		this.near = near;
		this.far = far;
	}
	//=============================================================================================
	
}
//*************************************************************************************************
