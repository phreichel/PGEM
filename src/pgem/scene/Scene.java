//*************************************************************************************************
package pgem.scene;
//*************************************************************************************************

import pgem.paint.Graphics;
import pgem.paint.Painter;

//*************************************************************************************************
public class Scene implements Painter {

	//=============================================================================================
	private Node root = new Node();
	//=============================================================================================

	//=============================================================================================
	public Node root() {
		return root;
	}
	//=============================================================================================
	
	//=============================================================================================
	public void paint(Graphics g) {
		
		g.perspective(60f, .6f, 1000f);

		g.push();
		g.translate(2, -2, -10);
		g.color(1, 0, 0, 1);
		g.box(true, -1, -1, -1, 2, 2, 2);
		g.pop();

		g.push();
		g.rotate(10f);
		g.translate(-2, 2, -10);
		g.color(0, 1, 1, 1);
		g.box(true, -1, -1, -1, 2, 2, 2);
		g.pop();
		
		root.paint(g);
		
	}
	//=============================================================================================

}
//*************************************************************************************************
