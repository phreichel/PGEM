//*************************************************************************************************
package pgem.paint;
//*************************************************************************************************

//*************************************************************************************************
public class TextData {

	//=============================================================================================
	private String font;
	private String text;
	private float width;
	private float height;
	private float baseline;
	private float ascent;
	private float descent;
	private float leading;
	//=============================================================================================

	//=============================================================================================
	public void set(
			String font,
			String text,
			float width,
			float height,
			float baseline,
			float ascent,
			float descent,
			float leading) {
		this.font = font;
		this.text = text;
		this.width = width;
		this.height = height;
		this.baseline = baseline;
		this.ascent = ascent;
		this.descent = descent;
		this.leading = leading;
	}
	//=============================================================================================
	
	//=============================================================================================
	public String font() {
		return font;
	}
	//=============================================================================================

	//=============================================================================================
	public String text() {
		return text;
	}
	//=============================================================================================

	//=============================================================================================
	public float width() {
		return width;
	}
	//=============================================================================================

	//=============================================================================================
	public float height() {
		return height;
	}
	//=============================================================================================

	//=============================================================================================
	public float baseline() {
		return baseline;
	}
	//=============================================================================================

	//=============================================================================================
	public float ascent() {
		return ascent;
	}
	//=============================================================================================

	//=============================================================================================
	public float descent() {
		return descent;
	}
	//=============================================================================================

	//=============================================================================================
	public float leading() {
		return leading;
	}
	//=============================================================================================
	
}
//*************************************************************************************************
