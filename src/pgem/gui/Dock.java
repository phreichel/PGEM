//*************************************************************************************************
package pgem.gui;
//*************************************************************************************************

//*************************************************************************************************
public class Dock {

	//=============================================================================================
	public static final Dock TOP_LEFT = new Dock(0, 0, 1, 1);
	public static final Dock TOP_RIGHT = new Dock(1, 1, 1, 1);
	public static final Dock BOTTOM_LEFT = new Dock(0, 0, 0, 0);
	public static final Dock BOTTOM_RIGHT = new Dock(1, 1, 0, 0);
	public static final Dock SCALE_LEFT = new Dock(0, 0, 1, 0);
	public static final Dock SCALE_RIGHT = new Dock(1, 1, 1, 0);
	public static final Dock SCALE_TOP = new Dock(0, 1, 1, 1);
	public static final Dock SCALE_BOTTOM = new Dock(0, 1, 0, 0);
	public static final Dock SCALE = new Dock(0, 1, 1, 0);
	//=============================================================================================
	
	//=============================================================================================
	private float left = 0;
	private float right = 0;
	private float top = 0;
	private float bottom = 0;
	//=============================================================================================

	//=============================================================================================
	public Dock() {}
	//=============================================================================================

	//=============================================================================================
	public Dock(Dock dock) {
		set(dock);
	}
	//=============================================================================================
	
	//=============================================================================================
	public Dock(float left, float right, float top, float bottom) {
		set(left, right, top, bottom);
	}
	//=============================================================================================
	
	//=============================================================================================
	public float left() {
		return left;
	}
	//=============================================================================================

	//=============================================================================================
	public float right() {
		return right;
	}
	//=============================================================================================

	//=============================================================================================
	public float top() {
		return top;
	}
	//=============================================================================================

	//=============================================================================================
	public float bottom() {
		return bottom;
	}
	//=============================================================================================

	//=============================================================================================
	public void set(Dock dock) {
		set(dock.left(),
			dock.right(),
			dock.top(),
			dock.bottom());
	}
	//=============================================================================================

	//=============================================================================================
	public void set(float left, float right, float top, float bottom) {
		this.left = left;
		this.right = right;
		this.top = top;
		this.bottom = bottom;
	}
	//=============================================================================================
	
}
//*************************************************************************************************
