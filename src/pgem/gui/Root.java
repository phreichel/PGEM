package pgem.gui;
//*************************************************************************************************

//*************************************************************************************************
class Root extends Widget<Root> {

	//=============================================================================================
	private GUI gui = null;
	//=============================================================================================

	//=============================================================================================
	static final Root createRoot(GUI gui) {
		return new Root(gui);
	}
	//=============================================================================================
	
	//=============================================================================================
	public Root(GUI gui) {
		super(true);
		this.gui = gui;
	}
	//=============================================================================================

	//=============================================================================================
	public GUI gui() {
		return this.gui;
	}
	//=============================================================================================
	
}
//*************************************************************************************************
