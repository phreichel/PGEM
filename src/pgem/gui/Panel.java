//*************************************************************************************************
package pgem.gui;
//*************************************************************************************************

import javax.vecmath.Color3f;
import javax.vecmath.Color4f;
import javax.vecmath.Vector2f;

import pgem.paint.Graphics;

//*************************************************************************************************
public class Panel extends Widget<Panel> {

	//=============================================================================================
	private static final Vector2f ORIGIN = new Vector2f(0, 0);
	//=============================================================================================

	//=============================================================================================
	public static final Panel createPanel() {
		return new Panel();
	}
	//=============================================================================================
	
	//=============================================================================================
	private Color4f border = new Color4f();
	private Color4f background = new Color4f();
	//=============================================================================================

	//=============================================================================================
	public Panel() {
		super(true);
	}
	//=============================================================================================
	
	//=============================================================================================
	public Color4f border() {
		return border;
	}
	//=============================================================================================

	//=============================================================================================
	public Panel border(Color3f border) {
		this.border.set(
			border.x,
			border.y,
			border.z,
			1);
		return this;
	}
	//=============================================================================================
	
	//=============================================================================================
	public Panel border(Color4f border) {
		this.border.set(
			border.x,
			border.y,
			border.z,
			border.w);
		return this;
	}
	//=============================================================================================

	//=============================================================================================
	public Panel border(float r, float g, float b) {
		this.border.set(r, g, b, 1);
		return this;
	}
	//=============================================================================================
	
	//=============================================================================================
	public Panel border(float r, float g, float b, float a) {
		this.border.set(r, g, b, a);
		return this;
	}
	//=============================================================================================
	
	//=============================================================================================
	public Color4f background() {
		return background;
	}
	//=============================================================================================

	//=============================================================================================
	public Panel background(Color3f background) {
		this.background.set(
			background.x,
			background.y,
			background.z,
			1);
		return this;
	}
	//=============================================================================================
	
	//=============================================================================================
	public Panel background(Color4f background) {
		this.background.set(
			background.x,
			background.y,
			background.z,
			background.w);
		return this;
	}
	//=============================================================================================

	//=============================================================================================
	public Panel background(float r, float g, float b) {
		this.background.set(r, g, b, 1);
		return this;
	}
	//=============================================================================================
	
	//=============================================================================================
	public Panel background(float r, float g, float b, float a) {
		this.background.set(r, g, b, a);
		return this;
	}
	//=============================================================================================

	//=============================================================================================
	protected void styleWidget(Style style) {
		background(style.get(StyleColor.PANEL_BACKGROUND));
		border(style.get(StyleColor.PANEL_BORDER));
	}
	//=============================================================================================
	
	//=============================================================================================
	protected void paintWidget(Graphics g) {
		g.color(background);
		g.box(true, ORIGIN, size());
		g.color(border);
		g.box(false, ORIGIN, size());
	}
	//=============================================================================================

}
//*************************************************************************************************
