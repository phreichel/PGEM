//*************************************************************************************************
package pgem.gui;
//*************************************************************************************************

import javax.vecmath.Color3f;
import javax.vecmath.Color4f;

import pgem.paint.Graphics;

//*************************************************************************************************
public class Image extends Widget<Image> {

	//=============================================================================================
	public static final Image createImage() {
		return new Image();
	}
	//=============================================================================================

	//=============================================================================================
	public static final Image createImage(Icon icon) {
		var image = new Image();
		image.image(icon.name());
		return image;
	}
	//=============================================================================================
	
	//=============================================================================================
	private String image = null;
	private Color4f color = new Color4f();
	//=============================================================================================
	
	//=============================================================================================
	public Image() {
		super();
	}
	//=============================================================================================

	//=============================================================================================
	public String image() {
		return image;
	}
	//=============================================================================================

	//=============================================================================================
	public Image image(String image) {
		this.image = image;
		return this;
	}
	//=============================================================================================

	//=============================================================================================
	public Color4f color() {
		return color;
	}
	//=============================================================================================

	//=============================================================================================
	public Image color(Color3f color) {
		this.color.set(
			color.x,
			color.y,
			color.z,
			1);
		return this;
	}
	//=============================================================================================
	
	//=============================================================================================
	public Image color(Color4f color) {
		this.color.set(
			color.x,
			color.y,
			color.z,
			color.w);
		return this;
	}
	//=============================================================================================

	//=============================================================================================
	public Image color(float r, float g, float b) {
		this.color.set(r, g, b, 1);
		return this;
	}
	//=============================================================================================
	
	//=============================================================================================
	public Image color(float r, float g, float b, float a) {
		this.color.set(r, g, b, a);
		return this;
	}
	//=============================================================================================

	//=============================================================================================
	protected void styleWidget(Style style) {
		color(style.get(StyleColor.IMAGE_COLOR));
	}
	//=============================================================================================
	
	//=============================================================================================
	protected void paintWidget(Graphics g) {
		g.color(color);
		g.image(image, 0, 0, size().x, size().y);
	}
	//=============================================================================================
	
}
//*************************************************************************************************
